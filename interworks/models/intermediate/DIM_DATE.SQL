select distinct
    DATE_TRUNC('day', "FLIGHTDATE") AS "FLIGHTDATE",
    EXTRACT(DAY FROM "FLIGHTDATE") AS "DAY",
    EXTRACT(WEEK FROM "FLIGHTDATE") AS "WEEK",
    EXTRACT(MONTH FROM "FLIGHTDATE") AS "MONTH",
    EXTRACT(QUARTER FROM "FLIGHTDATE") AS "QUARTER",
    EXTRACT(YEAR FROM "FLIGHTDATE") AS "YEAR",
from {{ ref('STG_FLIGHTS') }}